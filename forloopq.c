// program to print 0 to 12 using for loop 
#include<stdio.h>
int main(){
    // -- i ( pre decrement)
    // i -- ( post decrement)
for ( int i= 0 ; i<= 12; i= i++) { // i+1 , i+= 1 instead of i++
    
    printf("%d\n" ,i);
}
    return 0;
    
    
}
 //  post increment operator i++
 // ++ i means increase then use (pre increment operator )
